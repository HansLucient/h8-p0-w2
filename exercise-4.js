//WHILE

var jumlah = 0;

console.log('LOOPING PERTAMA');
while (jumlah < 20) {
  jumlah += 2;
  console.log(jumlah + ' ' + '- I love coding');
}


var jumlah = 22;
var deret = 2;

console.log('LOOPING KEDUA');
while (jumlah > 2) {
  jumlah -= deret;
  console.log(jumlah + ' ' + '- I will become full stack developer');
}
